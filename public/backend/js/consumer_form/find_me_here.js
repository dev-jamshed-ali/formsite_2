function validatePhoneNumber(phoneNumber, countryCode = 'US') {
    try {
        // Remove any non-digit characters
        const cleanNumber = phoneNumber.replace(/\D/g, '');
        
        // For US numbers, ensure it's not a fake number
        if (countryCode === 'US') {
            // Check for known invalid patterns
            if (cleanNumber.match(/^555/) || // 555 prefix
                cleanNumber.match(/^0{3}/) || // 000 prefix
                cleanNumber.match(/(\d)\1{9}/) || // Same digit repeated
                cleanNumber === '1234567890' || // Sequential numbers
                cleanNumber === '0987654321') { // Reverse sequential
                return {
                    isValid: false,
                    message: 'Invalid phone number pattern'
                };
            }

            // Check length for US numbers
            if (cleanNumber.length !== 10) {
                return {
                    isValid: false,
                    message: 'Phone number must be 10 digits'
                };
            }

            // Basic US format validation
            const areaCode = cleanNumber.substring(0, 3);
            const prefix = cleanNumber.substring(3, 6);
            
            // Area code can't start with 0 or 1
            if (areaCode.startsWith('0') || areaCode.startsWith('1')) {
                return {
                    isValid: false,
                    message: 'Invalid area code'
                };
            }

            // Prefix can't start with 0 or 1
            if (prefix.startsWith('0') || prefix.startsWith('1')) {
                return {
                    isValid: false,
                    message: 'Invalid prefix'
                };
            }

            return {
                isValid: true,
                formattedNumber: `(${areaCode}) ${prefix}-${cleanNumber.substring(6)}`,
                areaCode: areaCode,
                mainNumber: cleanNumber.substring(3)
            };
        }

        // For non-US numbers (basic validation)
        return {
            isValid: cleanNumber.length >= 10 && cleanNumber.length <= 15,
            message: 'Invalid phone number length',
            formattedNumber: phoneNumber
        };
    } catch (error) {
        console.log(error, "error");
        return {
            isValid: false,
            message: 'Invalid phone number'
        };
    }
}

// Update primary phone validation
document.getElementById('primary_mobile_number').addEventListener('input', function() {
    const result = validatePhoneNumber(this.value);
    const errorElement = this.closest('.form-group').querySelector('.text_danger');
    
    if (result.isValid) {
        this.value = result.formattedNumber;
        this.classList.add('valid');
        this.classList.remove('invalid');
        errorElement.textContent = '';
        this.setCustomValidity('');
    } else {
        this.classList.remove('valid');
        this.classList.add('invalid');
        errorElement.textContent = result.message;
        this.setCustomValidity(result.message);
    }
});

